        <div class="d-article-content">
            <p class="phase">2月21日，著名科技评论杂志 MIT Technology Review（以下简称 MIT TR）首次中美两国同步发布了年度“全球十大突破性技术”榜单。而今年这份重量级的科技榜单中，人工智能领域的相关技术毫不意外地成为了热点，其中“刷脸支付”这一项被中国企业全盘拿下，作为刷脸技术的主要研究者入选，同时入选的还有百度、科大讯飞、阿里巴巴。
            </p>
            <p class="phase">  
            全球十大突破性技术评选由 MIT TR 于2001年发起，17年中历届榜单中的许多技术已经走向市场成为主导产业的原动力，极大地推动了经济社会发展和科技创新，同时榜单也成为很多机构和政府判断科技商业化发展趋势的重要依据。如何定义「突破」？ MIT Technology Review 的主编 Jason Pontin 回答：“它们能够给人们带来高质量的运用科技的解决方案。”
            </p>
            <p class="img phase">
              <img src="images/dynamics_banner_2.jpg">
            </p>
            <p>
              为什么是“刷脸”
              说起来人脸识别的历史也不算短，最早1888 年实验心理学家 Francis Galton 就在《Nature》杂志上发表了关于利用人脸进行身份识别的文章，但探索的是人类自身对于人脸的识别能力，直到二十世纪中后期学者们才开始研究通过技术来实现自动人脸识别。从机械化识别到“特征脸”，人脸识别技术经历了数十年缓慢的发展过程，却鲜有现实应用价值。而今天，深度学习（2013年列为全球十大突破技术首位）的进步为解决大规模数据识别，尤其是传统的计算机视觉问题带来了新的思路。通过深度学习模型，人脸识别的准确率也得到了质的提升。
            </p>
            <p>
              2014年，人脸识别算法就超过了人类自身的识别能力，但还没有使其能够成为“突破性技术”，为何“刷脸”+支付却得到了行业的认可？正如 Jason 所说，技术的价值和突破点体现在是否能够给人们带来高质量运用科技的解决方案，就是说技术的进步不仅仅在于算法的精进，更多的是技术能够给我们带来什么实际价值——现在，就是刷脸技术发挥社会价值和商业价值最恰当的时期。
            </p>

